const test = require('ava')
const { union, create, fromPoints } = require('./index')

test('geom3: union() with zero geometries should throw error', (t) => {
  t.throws(() => {
    union()
  }, 'union requires one or more geometries')
})

test('geom3: union() with one geometry should return the same', (t) => {
  const obj1 = create()
  const ret1 = union(obj1)
  t.is(obj1, ret1)
})

test('geom3: union() should create proper union from empty geometries', (t) => {
  const obj1 = create()
  const obj2 = create()
  const obj3 = create()

  // one empty geometry
  const ret1 = union(obj1)
  const exp1 = {
    polygons: [],
    isCanonicalized: true,
    isRetesselated: true
  }
  t.deepEqual(ret1, exp1)

  // two empty geometries
  const ret2 = union(obj1, obj2)
  const exp2 = {
    polygons: [],
    isCanonicalized: true,
    isRetesselated: true
  }
  t.deepEqual(ret2, exp2)

  // three empty geometries
  const ret3 = union(obj1, obj2, obj3)
  const exp3 = {
    polygons: [],
    isCanonicalized: true,
    isRetesselated: true
  }
  t.deepEqual(ret3, exp3)
})

